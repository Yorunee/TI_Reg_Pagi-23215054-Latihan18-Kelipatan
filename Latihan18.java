
package Pertemuan5;


public class Latihan18 {
     public static void main(String[] args) {
        // Inisialisasi variabel awal
        double angka = 3.5;

        // Loop untuk mencetak angka kelipatan 3.5 hingga 35
        while (angka <= 35) {
            System.out.println(angka);
            angka += 3.5; // Tambah 3.5 setiap iterasi
        }
    }
}
    
